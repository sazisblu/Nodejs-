
// Declare a string variable and assign it a value
let StringVar = "Joe mama"; // Assign a string value to the variable
console.log(typeof StringVar); // Log the type of the variable to the console

// Declare a number variable and assign it a value
let numberVar = 10; // Assign a number value to the variable
console.log(typeof numberVar); // Log the type of the variable to the console

// Declare a boolean variable and assign it a value
let booleanVar = true; // Assign a boolean value to the variable
console.log(typeof booleanVar); // Log the type of the variable to the console

// Declare an object variable and assign it a value
let objectVar = { name: "sajan", age: 20 }; // Assign an object value to the variable
console.log(typeof objectVar); // Log the type of the variable to the console

// Declare an undefined variable
let undefinedVar; // Declare a variable without assigning a value
console.log(typeof undefinedVar); // Log the type of the variable to the console

// Declare a null variable and assign it a value
let nullVar = null; // Assign a null value to the variable
console.log(typeof nullVar); // Log the type of the variable to the console